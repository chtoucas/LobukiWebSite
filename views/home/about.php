<h1>Manu et Laurine, créatrices de Lobuki racontent</h1>
<p id=quote>
"C'est en juin 2009 que l'idée de Lobuki est née, lors d'une réunion entre cousins !
Lobuki c'est donc avant tout une chouette histoire de famille ! C'est l'association
de deux personnalités, l'une liée à l'imaginaire et l'autre au réel. Laurine conçoit
les dessins et Manu les rend vivants ! Nous choisissons les thèmes de nos stickers en
fonction de nos envies et de nos propres interrogations. Lobuki, c'est des dessins
plein de poésie qui tentent d'expliquer simplement les choses qui nous entourent..."
</p>
